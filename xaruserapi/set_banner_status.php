// Sets the status of a banner
  function commerce_userapi_set_banner_status($banners_id, $status) {
    if ($status == '1') {
      return new xenQuery("update " . TABLE_BANNERS . " set status = '1', date_status_change = now(), date_scheduled = NULL where banners_id = '" . $banners_id . "'");
    } elseif ($status == '0') {
      return new xenQuery("update " . TABLE_BANNERS . " set status = '0', date_status_change = now() where banners_id = '" . $banners_id . "'");
    } else {
      return -1;
    }
  }
